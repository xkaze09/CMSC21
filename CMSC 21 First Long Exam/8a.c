#include <stdio.h>

int main() {
    /* Start of variable declaration */
    int a = 2;
    int b = 3;
    /* End of variable declaration */

    /* Start of code snippet */
    if (a == 2 && b == 3)
        printf("*****");
      else
        printf("-----");
    printf(">>>>>");
    printf("<<<<<");
    /* End of code snippet */

    return 0;
}
