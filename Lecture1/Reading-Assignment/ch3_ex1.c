// For printf function
#include <stdio.h>

int main(void) {
    // Variable Declaration and Initialization
    // using int and float data types
    int i, j;
    float x, y;
    i = 10;
    j = 20;
    x = 43.2892f;
    y = 5527.0f;
    // End of Variable Declaration and Initialization

    // Display output by format string
    printf("i = %d, j = %d, x = %f, y = %f\n", i, j, x, y);
}
